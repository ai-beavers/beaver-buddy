// Baby-beaver pixel maps: one 32x32 string grid per frame, one char per
// pixel (palette.ts key, or '.' for transparent). Right-facing only — the
// renderer mirrors for left-facing (assets/STYLE.md, once written).
//
// Design: chibi side profile, ~16px tall. Flat wide tail (back-left, with a
// couple of lighter scute-line pixels for the segmented-tail texture),
// rounded double-ear bump, snout with two protruding teeth, single eye with
// a cyan highlight, teal-gray belly patch. Every silhouette pixel earns its
// 1px outline by touching a transparent neighbor (see the authoring notes
// in the BL-3 plan) — there is no separate hand-drawn outline pass here,
// the shapes were composed so the outline falls out of the silhouette.
//
// Frame order per animation matches ANIMATION_ORDER below.

